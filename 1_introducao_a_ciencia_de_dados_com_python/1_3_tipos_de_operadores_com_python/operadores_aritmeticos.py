produto_1 = 20
produto_2 = 10

#Soma
print(" A soma dos produtos é", produto_1 + produto_2) 

#Subtração
print(" A subtração dos produtos é", produto_1 - produto_2) 

#Divisão
print(" A divisão entre os produtos é", produto_1 / produto_2) 

#Divisão Inteira
print(" A divisão inteira entre os produtos é", produto_1 // produto_2) 

#Multiplicação
print(" A multiplicação entre produtos é", produto_1 * produto_2) 

#Resto
print(" A módulo dos produtos é", produto_1 % produto_2) 

#Exponenciação
print("O produto 1 elevado ao produto 2 é igual a:", produto_1 ** produto_2) 

